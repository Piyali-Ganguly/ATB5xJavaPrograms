package src.basics_03;

// Compound operators-> +=,-=, *=, /=, %=

public class Lab012 {
    public static void main(String[] args) {
        int a = 30;
        int b = 40;
        int c = 20;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);

        a+= 30;
        b-= 20;
        c*= 5;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
