package src.basics_02;

// Characters

public class Lab007 {
    public static void main(String[] args) {
        char c1 = 'A';  // character
        System.out.println(c1);

        char c2 = '$';   // special character
        System.out.println(c2);

        char c3 = '\n';   // new line
        System.out.println(c3);

        char c4 = '\u0000';   // unicode
        System.out.println(c4);

    }
}
