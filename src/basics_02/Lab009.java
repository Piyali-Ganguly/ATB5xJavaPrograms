package src.basics_02;

//Escape sequence
public class Lab009 {
    public static void main(String[] args) {

        char c1 ='\n';   // new line
        char c2 ='\t';   // add tab
        char c3 = '\b';  // backlash, deletes one character
        char c4 = '\r';  // return
        System.out.println("Piyali" + c1 + "Ganguly");
        System.out.println("Piyali" + c2 + "Ganguly");
        System.out.println("Piyali" + c3 + "Ganguly");
        System.out.println("Piyali" + c4 + "Ganguly");
    }
}
