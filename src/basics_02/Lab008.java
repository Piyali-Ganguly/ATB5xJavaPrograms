package src.basics_02;

// Operators-Arithmetic
public class Lab008 {
    public static void main(String[] args) {

        double a = 34;
        double b = 10;

        double sum = a+b;
        System.out.println(sum);

        double sub = a-b;
        System.out.println(sub);

        double mul = a*b;
        System.out.println(mul);

        double div = a/b;
        System.out.println(div);

        double mod = a%b;
        System.out.println(mod);
    }
}